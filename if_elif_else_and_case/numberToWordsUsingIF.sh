read -p "enter single digit number: " num

if [ $num == 0 ]
then
	echo "ZERO"

elif [ $num == 1 ]
then
	echo "ONE"

elif [ $num == 3 ]
then
        echo "THREE"

elif [ $num == 4 ]
then
        echo "FOUR"

elif [ $num == 5 ]
then
        echo "FIVE"

elif [ $num == 6 ]
then
        echo "SIX"

elif [ $num == 7 ]
then
        echo "SEVEN"

elif [ $num == 8 ]
then
        echo "EIGHT"

elif [ $num == 9 ]
then
        echo "NINE"
fi
